CREATE TABLE STUDENT(
   S_ID NUMBER CONSTRAINT S_ID_PK PRIMARY KEY,
   S_NAME VARCHAR(30) NOT NULL,
   S_PWD VARCHAR(30) NOT NULL,
   S_PHONE VARCHAR(40),
   S_EMAIL VARCHAR(50)
);
CREATE TABLE ENROLL(
   C_ID NUMBER,
   S_ID NUMBER,
   C_NAME VARCHAR(30) NOT NULL,
   C_ID_NO NUMBER,
   C_UNIT NUMBER NOT NULL,
   C_YEAR NUMBER,
   C_SEMESTER NUMBER,
   C_TIME VARCHAR(60) NOT NULL,
   C_ROOM VARCHAR(10) NOT NULL,
   CONSTRAINT ENROLL_PK PRIMARY KEY(C_ID, S_ID, C_ID_NO,C_YEAR,C_SEMESTER),
   CONSTRAINT FK_S_ID FOREIGN KEY(S_ID)
   REFERENCES STUDENT(S_ID)
);
CREATE TABLE PROFESSOR(
   P_ID NUMBER CONSTRAINT P_ID_PK PRIMARY KEY,
   P_NAME VARCHAR(30) NOT NULL,
   P_PWD VARCHAR(30) NOT NULL,
   P_PHONE VARCHAR(40), 
   P_EMAIL VARCHAR(50) 
);
CREATE TABLE TEACH(
   C_ID NUMBER ,
   P_ID NUMBER ,
   C_NAME VARCHAR(30) NOT NULL,
   C_ID_NO NUMBER ,
   C_UNIT NUMBER NOT NULL,
   C_YEAR NUMBER NOT NULL,
   C_SEMESTER NUMBER NOT NULL,
   C_TIME VARCHAR(60) NOT NULL,
   C_ROOM VARCHAR(10) NOT NULL,
   C_MAX NUMBER NOT NULL,
   CONSTRAINT TEACH_PK PRIMARY KEY(C_ID, C_ID_NO, C_YEAR, C_SEMESTER),
   CONSTRAINT FK_P_ID FOREIGN KEY(P_ID)
   REFERENCES PROFESSOR(P_ID)
);

CREATE TABLE SCHEDULE(
   S_ID NUMBER,
   C_YEAR NUMBER,
   C_SEMESTER NUMBER,
   S_PERIOD NUMBER,
   S_MON NUMBER,
   S_TUE NUMBER,
   S_WEN NUMBER,
   S_THU NUMBER,
   S_FRI NUMBER,
   CONSTRAINT SCHEDULE_PK PRIMARY KEY(S_ID,C_YEAR,C_SEMESTER,S_PERIOD)
);

CREATE TABLE MESSAGE(
   M_SENDER NUMBER,
   M_RECEIVER NUMBER,
   M_CHECKED NUMBER,
   M_SENDTIME VARCHAR(50),
   M_TITLE VARCHAR(50),
   M_TEXT VARCHAR(150)
);


